<p>
Специально не&nbsp;убирал крякозябру из&nbsp;последнего пункта&nbsp;&mdash; на&nbsp;это нужно обращать внимание. В&nbsp;остальном&nbsp;&mdash; просто замечательно! Без лишних телодвижений была выполнена весьма рутинная работа. Казалось&nbsp;бы, замечательная программа. Казалось&nbsp;бы...</p>
<p>
Но&nbsp;для кого она? Я&nbsp;сейчас именно про плагин для Word. Студентам вряд&nbsp;ли пригодится&nbsp;&mdash; у&nbsp;них в&nbsp;литобзорах/дипломах не&nbsp;слишком много PDF-статей. Да, можно pdf создать вручную, но&nbsp;проще уж&nbsp;использовать метод перекрестных ссылок, который <a href="http://anabot.ru/kvak/30/638/">
был описан в&nbsp;прошлой статье</a>
.</p>
<p>
Может, плагин пригодится аспирантам и&nbsp;другим ученым? В&nbsp;этом случае, конечно, более оправдано, ведь процент ссылок на&nbsp;статьи в&nbsp;наших работах весьма большой. Однако, представьте себе такую вещь: нашел я&nbsp;хорошую монографию, решил добавить цитату. И&nbsp;что? Придется создавать pdf-документ. По-быстрому, вручную, вставить источник литературы уже нельзя (а&nbsp;метод перекрестных ссылок это позволяет сделать вообще не&nbsp;заморачиваясь).</p>
<p>
Вы&nbsp;скажете, что так даже лучше&nbsp;&mdash; программа таким образом стимулирует создавать собственный каталог-библиотеку, что в&nbsp;конечном счете сослужит хорошую службу? Возможно, не&nbsp;спорю. Но&nbsp;по&nbsp;мне&nbsp;&mdash; слишком муторно.</p>
<p>
Последняя ложка дегтя&nbsp;&mdash; плагин представляет собой VBA-скрипт (макрос), который выполняется в&nbsp;бешеное число итераций. Любите нажимать кнопку &laquo;отмена&raquo; в&nbsp;Word? Забудьте теперь про нее! Каждое добавление ссылки&nbsp;&mdash; это&nbsp;14 (!) действий.</p>
<p>
Впрочем, этим плагином <a href="https://ru.wikipedia.org/wiki/Mendeley#.D0.92.D0.BE.D0.B7.D0.BC.D0.BE.D0.B6.D0.BD.D0.BE.D1.81.D1.82.D0.B8">
возможности программы не&nbsp;ограничиваются</a>
. Если будет интересно, напишу пару статей о&nbsp;каталогизаторе и&nbsp;международной научной соц. сети.</p>
