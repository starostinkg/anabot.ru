<p>
Проделаем &laquo;Insert Citation&raquo; еще в&nbsp;паре мест. После переходим на&nbsp;другую страницу и&nbsp;нажимаем &laquo;Insert Bibliography&raquo;. И&nbsp;вот она, магия PDF-статей:</p>
<p style="text-align:center;">
<a href="http://anabot.ru/uploads/1424391185_mendeley-9.PNG">
