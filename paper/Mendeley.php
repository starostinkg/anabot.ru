<p>
Мы-то ожидали увидеть единичку в&nbsp;квадратных скобках, а&nbsp;тут... Что-то пошло не&nbsp;так? Спокойствие, глубокий вдох... Посмотрите на&nbsp;список Style (отмечено красным прямоугольником). Просто был выбран формат ссылок, принятый в&nbsp;США. У&nbsp;нас есть свой гост. У&nbsp;нас есть. А&nbsp;вот в&nbsp;Mendeley&nbsp;&mdash; нету. И&nbsp;где это я&nbsp;читал, что у&nbsp;них все хорошо с&nbsp;адаптацией под наши условия? :) Однако сегодня любой знает: если чего-то нет, то&nbsp;это &laquo;что-то&raquo; можно... правильно, скачать! Потому выбираем в&nbsp;стилях последний пункт &laquo;More Styles...&raquo;.</p>
<p style="text-align:center;">
<a href="http://anabot.ru/uploads/1424393524_mendeley-7.PNG">
<img alt="" src="http://anabot.ru/uploads/1424393524_mendeley-7.PNG" style="height:446px;width:652px;" />
</a>
</p>
<p>
В&nbsp;открывшемся окне щелкаем по&nbsp;вкладке &laquo;Get More Styles&raquo;, вводим ссылку на&nbsp;ГОСТ-Р-7-0-11-2011 (http://chetvericov.ru/files/GOST-R-7_0_11-2011_-_numeric_-_alphabetic.csl)<sup>
[<a href="http://anabot.ru/files/all_files/GOST-R-7_0_11-2011.csl">
зеркало</a>
]</sup>
и&nbsp;по&nbsp;кнопке Download устанавливаем этот стиль. Жмем Done и&nbsp;видим, что стиль автоматически применился:</p>
<p style="text-align:center;">
<a href="http://anabot.ru/uploads/1424390672_mendeley-8.PNG">
