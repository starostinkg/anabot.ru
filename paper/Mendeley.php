<p>
В&nbsp;комментариях к&nbsp;статье <a href="/paper/30/638/">
&laquo;Как расставлять ссылки в&nbsp;литобзоре при помощи Word&raquo;</a>
посоветовали программу Mendeley, как инструмент, в&nbsp;котором есть &laquo;есть много полезных функций, сильно упрощающих жизнь студентам и&nbsp;учёным&raquo;. Ну&nbsp;что&nbsp;же, давайте прикоснемся к&nbsp;прекрасному.</p>
<p>
Заходим на&nbsp;сайт <a href="http://www.mendeley.com/">
http://www.mendeley.com/</a>. Первое, что бросается в&nbsp;глаза&nbsp;&mdash; предложение зарегистрироваться. К&nbsp;сожалению, без регистрации программа бесполезна чуть более, чем полностью, потому жмите на&nbsp;большую красную кнопку &quot;<a title="Sign up" class="large-button" href="http://www.mendeley.com/join/?trackingUrl=%2Fjoin%2Fformreg%2Fhp%2Fintro%2F">
Create a&nbsp;free account</a>&laquo;. Там надо заполнить несколько полей, указать ученую степень и&nbsp;область исследования. После чего автоматически начнется загрузка программы. Пока прога скачивается, можете проверить почту&nbsp;&mdash; должна придти ссылка для подтверждения регистрации. Жмем на&nbsp;ссылку&nbsp;&mdash; поздравляю, вы&nbsp;во&nbsp;что-то вляпались! Давайте разбираться, во&nbsp;что.</p>
<p>
Программа скачана, приступаем к&nbsp;установке. Ничего необычного&nbsp;&mdash; &laquo;Next &gt; Agree &gt; Next &gt; Install&raquo;. Установка чистая, без навязчивых предложений сторонних программ типа mail-агента, что радует. Установили, запускаем. Выскакивает окно авторизации, где надо ввести мыло/пароль. Далее предлагается загрузить вашу библиотеку PDF-документов&nbsp;&mdash; это можно отложить на&nbsp;потом, нажав кнопку &laquo;Skip&raquo; в&nbsp;левом углу. Программа заботливо предложит топ-20 статей по&nbsp;выбранной области исследования, которые можно отметить галками для 
начального пополнения библиотеки&nbsp;&mdash; советую добавить для примера любые 3-4 публика<s>ка</s>шки (не&nbsp;обольщайтесь, добавятся скорее всего только их&nbsp;аннотации), чтобы было с&nbsp;чем экспериментировать.</p>
<p>
Ура, свершилось! Программа наконец-то запущена.Выбираем <strong>
Tools &gt; Install MS&nbsp;Word Plugin</strong>
(автор&nbsp;&mdash; зззануда, столько текста, где картинки?!)</p>
<p style="text-align:center;">
<a href="http://anabot.ru/uploads/1424383496_mendeley-1.PNG">
<img alt="" src="http://anabot.ru/uploads/1424383496_mendeley-1.PNG" style="height:529px;width:688px;" />
</a>
</p>
<p>
Если у&nbsp;вас открыт Word или Outlook, то&nbsp;выскочит такое окно:</p>
<p style="text-align:center;">
<a href="http://anabot.ru/uploads/1424383874_mendeley-2.PNG">
<img alt="" src="http://anabot.ru/uploads/1424383874_mendeley-2.PNG" style="height:139px;width:428px;" />
</a>
</p>
<p>
Нажмите &laquo;No&raquo;, потом &laquo;OK&raquo;, закройте указанные программы и&nbsp;попробуйте снова. Установка проходит быстро&nbsp;&mdash; тут&nbsp;же появляется окно (я&nbsp;немного его русифицировал), подсказывающее куда встал плагин:</p>
<p style="text-align:center;">
<a href="http://anabot.ru/uploads/1424384800_mendeley-3.PNG">
<img alt="" src="http://anabot.ru/uploads/1424384800_mendeley-3.PNG" style="height:425px;width:433px;" />
</a>
</p>
<p>
Открываем Word&nbsp;&mdash; и&nbsp;действительно, во&nbsp;вкладке &laquo;Ссылки&raquo; появился блок Менделея. Что&nbsp;же, давайте испытывать, как оно работает. Для примера будем использовать какой-то текст&nbsp;&mdash; я&nbsp;тупо надергал несколько цитат из&nbsp;научных статей. Итак, ставим курсор в&nbsp;конец предложения, куда хотим поставить ссылку, нажимаем на&nbsp;кнопку &laquo;Insert Citation&raquo;&nbsp;&mdash; появляется окно Менделея:</p>
<p style="text-align:center;">
<a href="http://anabot.ru/uploads/1424388482_mendeley-4.PNG">
<img alt="" src="http://anabot.ru/uploads/1424388482_mendeley-4.PNG" style="height:445px;width:562px;" />
</a>
</p>
<p>
Выбор небогат, потому&nbsp;&mdash; &laquo;Go&nbsp;to&nbsp;Mendeley&raquo;. Откроется библиотека:</p>
<p style="text-align:center;">
<a href="http://anabot.ru/uploads/1424388937_mendeley-5.PNG">
<img alt="" src="http://anabot.ru/uploads/1424388937_mendeley-5.PNG" style="height:323px;width:559px;" />
</a>
</p>
<p>
Выбираем статью, на&nbsp;которую хотим ссылаться (удерживая Ctrl, можно выбрать несколько статей), теперь клацаем по&nbsp;кнопке &laquo;Cite&raquo; и... УЖАС, ЧТО С&nbsp;МОИМИ ГЛАЗАМИ?!!!!</p>
<p style="text-align:center;">
<a href="http://anabot.ru/uploads/1424389518_mendeley-6.PNG">
<img alt="" src="http://anabot.ru/uploads/1424389518_mendeley-6.PNG" style="height:447px;width:564px;" />
